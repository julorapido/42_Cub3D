#include "libft.h"

char	*ft_strjoin_free(char *s1, char *s2)
{
	char	*new;
	size_t	i;
	size_t	j;

	if (!ft_strlen(s2) || !s2)
	{
		free(s2);
		return (s1);
	}
	new = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		new[i + j] = s2[j];
		j++;
	}
	new[i + j] = '\0';
	free(s1);
	free(s2);
	return (new);
}

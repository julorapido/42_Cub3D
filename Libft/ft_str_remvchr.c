#include "libft.h"

char	*ft_str_remvchr(char *s, char a, char b)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (s[i])
	{
		if (!(s[i] == a || s[i] == b))
		{
			s[j] = s[i];
			j++;
		}
		i++;
	}
	s[j] = '\0';
	return (s);
}
